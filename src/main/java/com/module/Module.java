package com.module;

public class Module {
	private int mid;          
    private String mname;     
    private String mdes;      
    private String mcode;
    
    
	public Module(int mid, String mname, String mdes, String mcode) {
		super();
		this.mid = mid;
		this.mname = mname;
		this.mdes = mdes;
		this.mcode = mcode;
	}


	
	public int getMid() {
		return mid;
	}


	public String getMname() {
		return mname;
	}


	public String getMdes() {
		return mdes;
	}


	public String getMcode() {
		return mcode;
	}  
	
	
	
	
    
    
    
}
