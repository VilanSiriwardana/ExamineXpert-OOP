package com.paper;

public class Paper {
	private int pid;
	private int mid;
    private String pname;
    
	public Paper(int pid, int mid, String pname) {
		super();
		this.pid = pid;
		this.mid = mid;
		this.pname = pname;
	}

	public int getPid() {
		return pid;
	}

	public int getMid() {
		return mid;
	}

	public String getPname() {
		return pname;
	}
	
	
	
    
    
}
